package colorModel;

public class ColoreRGB {
	private int r,g,b;
	
	public ColoreRGB(int red,int green,int blue) {
		this.r=red;
		this.g=green;
		this.b=blue;
	}
	
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	public int getG() {
		return g;
	}
	public void setG(int g) {
		this.g = g;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}


}
